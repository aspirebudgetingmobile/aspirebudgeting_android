package com.aspirebudgetingmobile.aspirebudgeting.models;

public class SheetsListModel {
    String id, name;

    public SheetsListModel(String id, String name) {
        this.id = id;
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
